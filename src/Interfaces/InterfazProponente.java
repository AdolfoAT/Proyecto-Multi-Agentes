    public boolean validar()
    {
        String ID_Proponente=AP.IDProponente;
        String Titulo2 = AP.IP.Proyecto.getSelectedItem().toString();
        String Status ="";
        String Instruccion="SELECT * from reportedeinvestigacion where Titulo like '"+ Titulo2 +"' and Aspirante_idAspirante= '"+ID_Proponente+"' ";
       try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(Instruccion);
            if(rs.next()) {
              Status=rs.getString(11);
            }
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, "Error: "+e);
        }
        return !Status.equals("Terminado");
    }
    
