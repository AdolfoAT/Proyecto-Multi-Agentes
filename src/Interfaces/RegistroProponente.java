    public boolean Validar()
    {
        if(Nombre.getText().length()==0|| ApellidoPaterno.getText().length()==0|| ApellidoMaterno.getText().length()==0 || Edad.getText().length()==0 || Telefono.getText().length()==0 || CorreoElectronico.getText().length()==0 || TituloObtenido.getText().length()==0 || Usuario.getText().length()==0 ||Password.getText().length()==0)
        {
            return false;
        }
        else
        {
            int Edad1=Integer.parseInt(Edad.getText());
            if(Edad1<18 || Edad1>90)
            {
              return false;
            }
            else
            {
                return Masculino.isSelected() || Femenino.isSelected();
            }
        }
        
    }
    
